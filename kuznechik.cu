void set_key(kuznechik::block keys[10], const unsigned char key[32])
{
	int i, j;
	kuznechik::block c, x, y, z;

	for (i = 0; i < 16; i++)
	{
		x.c[i] = key[i];
		y.c[i] = key[i + 16];
	}

	keys[0].ull[0] = x.ull[0];
	keys[0].ull[1] = x.ull[1];
	keys[1].ull[0] = y.ull[0];
	keys[1].ull[1] = y.ull[1];

	for (i = 1; i <= 32; i++)
	{

		c.ull[0] = 0;
		c.ull[1] = 0;
		c.c[15] = i;
		l(c);

		z.ull[0] = x.ull[0] ^ c.ull[0];
		z.ull[1] = x.ull[1] ^ c.ull[1];
		for (j = 0; j < 16; j++)
			z.c[j] = S[z.c[j]];
		l(z);
		z.ull[0] ^= y.ull[0];
		z.ull[1] ^= y.ull[1];

		y.ull[0] = x.ull[0];
		y.ull[1] = x.ull[1];

		x.ull[0] = z.ull[0];
		x.ull[1] = z.ull[1];

		if ((i & 7) == 0)
		{
			keys[(i >> 2)].ull[0] = x.ull[0];
			keys[(i >> 2)].ull[1] = x.ull[1];
			keys[(i >> 2) + 1].ull[0] = y.ull[0];
			keys[(i >> 2) + 1].ull[1] = y.ull[1];
		}
	}
}

static uint8_t multiply(uint8_t x, uint8_t y)
{
	uint8_t z;

	z = 0;
	while (y)
	{
		if (y & 1)
			z ^= x;
		x = (x << 1) ^ (x & 0x80 ? 0xC3 : 0x00);
		y >>= 1;
	}

	return z;
}


static void l(kuznechik::block& w)
{
	int i, j;
	uint8_t x;

	for (j = 0; j < 16; j++) 
	{
		x = w.c[15];	

		for (i = 14; i >= 0; i--)
		{
			w.c[i + 1] = w.c[i];
			x ^= multiply(w.c[i], kuz_lvec[i]);
		}
		w.c[0] = x;
	}
}

